CREATE DATABASE eval_s1;
USE eval_s1;
CREATE TABLE book (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    slug VARCHAR(255) NOT NULL UNIQUE,
    description TEXT NOT NULL,
    `date` DATE NOT NULL,
    PRIMARY KEY(id),
    CONSTRAINT uq_book UNIQUE(title, author)
);
ALTER TABLE book DROP INDEX uq_book;
CREATE TABLE user (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    username VARCHAR(255) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    PRIMARY KEY(id)
);
CREATE TABLE category (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    category VARCHAR(255) NOT NULL UNIQUE,
    slug VARCHAR(255) NOT NULL UNIQUE,
    PRIMARY KEY(id)
);
ALTER TABLE `book` ADD `category_id` INT UNSIGNED NOT NULL;
ALTER TABLE `book` ADD CONSTRAINT `fk_book_category` FOREIGN KEY(`category_id`) REFERENCES `category`(`id`);
CREATE TABLE tag (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    tag VARCHAR(255) NOT NULL UNIQUE,
    slug VARCHAR(255) NOT NULL UNIQUE,
    PRIMARY KEY(id)
);
CREATE TABLE book_tag (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    tag_id INT UNSIGNED NOT NULL,
    book_id INT UNSIGNED NOT NULL,
    PRIMARY KEY(id),
    CONSTRAINT `fk_book_tag_tag` FOREIGN KEY(`tag_id`) REFERENCES `tag`(`id`) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT `fk_book_tag_book` FOREIGN KEY(`book_id`) REFERENCES `book`(`id`) ON DELETE CASCADE ON UPDATE CASCADE
);
